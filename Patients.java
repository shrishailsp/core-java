class Patients{

private int id;
private String name;
private int age;
private String purpos;
private String gender;

public Patients(){

}
public void setId(int id){
this.id=id;
}
public int getId(){
return id;
}

public void setName(String name){
this.name=name;
}
public String getName(){
return name;
}

public void setAge(int age){
this.age=age;
} 
public int getAge(){
return age;
}

public void setPurpose(String purpos){
this.purpos=purpos;
}
public String getPurpose(){
return purpos;
}

public void setGender(String gender){
this.gender=gender;
}
public String getGender(){
return gender;
}
}